def _resolve_device_ip(device: str) -> str:
    if device in _DEVICE_IP_CACHE:
        return _DEVICE_IP_CACHE[device]
    if re.match(r'\d+\.\d+\.\d+\.\d+', device):
        _DEVICE_IP_CACHE[device] = device
        return device
    if DEVICES_DB.exists():
        db = json.loads(DEVICES_DB.read_text())
        for d in db if isinstance(db, list) else db.get("devices", []):
            name = d.get("name", d.get("hostname", ""))
            if name == device or name.lower() == device.lower():
                ip = d.get("ip", d.get("mgmt_ip", ""))
                if ip:
                    _DEVICE_IP_CACHE[device] = ip
                    return ip
    _DEVICE_IP_CACHE[device] = device
    return device


def _get_session(device: str) -> InteractiveSSHSession:
    """Get or create a persistent SSH session for the device."""
    ip = _resolve_device_ip(device)
    sess = _SSH_SESSIONS.get(ip)
    if sess and sess.is_alive():
        return sess
    if sess:
        print(f"  [SSH] Reconnecting persistent session to {ip}...")
        try:
            sess.close()
        except Exception:
            pass
    else:
        print(f"  [SSH] Opening persistent session to {ip}...")
    sess = InteractiveSSHSession(ip, 'dnroot', _DNROOT_PWD, connect_timeout=15)
    _SSH_SESSIONS[ip] = sess
    print(f"  [SSH] Connected to {sess.hostname} ({ip}) -- prompt-based, 50ms poll")
    return sess


def _close_all_sessions():
    """Close all persistent SSH sessions."""
    for ip, sess in _SSH_SESSIONS.items():
        try:
            sess.close()
        except Exception:
            pass
    _SSH_SESSIONS.clear()


def run_show_command(device: str, command: str, retries: int = 3, retry_delay: int = 10) -> str:
    try:
        sess = _get_session(device)
        result = sess.send_command(
            command, timeout=120, auto_no_more=True,
            reconnect_attempts=retries, reconnect_interval=retry_delay,
        )
        if result.startswith("[SSH ERROR]"):
            print(f"  [SSH-FAIL] {command[:60]}... {result}")
        return result
    except Exception as e:
        ip = _resolve_device_ip(device)
        print(f"  [SSH-FAIL] {command[:60]}... persistent session error: {e}")
        return f"[ERROR] SSH to {device} ({ip}): {e}"


def run_ha_trigger(device: str, command: str, confirmation: str = "yes") -> str:
    """Send an HA trigger using a separate one-shot SSH connection.

    HA triggers (process restart, NCC switchover) can disrupt the management
    plane, so we use a disposable connection instead of the persistent session.
    The persistent session auto-reconnects on the next run_show_command call.
    """
    ip = _resolve_device_ip(device)
    print(f"  [TRIGGER] {command}")
    c = paramiko.SSHClient()
    c.set_missing_host_key_policy(paramiko.AutoAddPolicy())
    c.connect(ip, port=22, username='dnroot', password=_DNROOT_PWD,
              timeout=10, look_for_keys=False, allow_agent=False)
    sh = c.invoke_shell(width=300, height=48)
    time.sleep(1.5)
    if sh.recv_ready():
        sh.recv(65536)
    sh.send(command + '\n')
    time.sleep(3)
    out = b''
    end_t = time.time() + 10
    while time.time() < end_t:
        if sh.recv_ready():
            out += sh.recv(65536)
        else:
            time.sleep(0.3)
    text = out.decode('utf-8', errors='replace')
    lower = text.lower()
    if ('(yes/no)' in lower or '[yes/no]' in lower or '[y/n]' in lower
            or 'are you sure' in lower or 'confirm' in lower):
        print(f"  [TRIGGER] Confirmation prompt detected, sending '{confirmation}'")
        sh.send(confirmation.lower() + '\n')
        time.sleep(5)
        while sh.recv_ready():
            out += sh.recv(65536)
            time.sleep(0.3)
    c.close()

    # Mark persistent session for reconnect (HA trigger may have killed mgmt plane)
    sess = _SSH_SESSIONS.get(ip)
    if sess:
        try:
            sess.close()
        except Exception:
            pass
        del _SSH_SESSIONS[ip]
        print(f"  [TRIGGER] Persistent session invalidated -- will auto-reconnect on next command")

    result = strip_ansi(out.decode('utf-8', errors='replace'))
    if 'ERROR:' in result:
        raise RuntimeError(f"HA trigger command FAILED: {result.strip()}")
    return result


